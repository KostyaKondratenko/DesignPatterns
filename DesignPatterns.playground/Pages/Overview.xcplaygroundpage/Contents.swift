 # Overview
 - - - - - - - - - -
 There are three types of design patterns:
 
 1. **Structural patterns** describe how objects are composed to form larger subsystems.
 Examples of structural patterns are:
 
    a) **Fundamental**
 
 - [Model-view-controller (MVC)](Model-View-Controller)
 
    b) **Intermediate**
 
 - [Adapter](Adapter)
 
    c) **Advanced**
 
 2. **Behavioural patterns** describe how objects communicate with each other.
 Examples of behavioural patterns are:
 
    a) **Fundamental**
 
 - [Delegation](Delegation)
 - [Strategy](Strategy)
 - [Observer](Observer)
 - [Memento](Memento)
 
    b) **Intermediate**
 
    c) **Advanced**
 
 3. **Creational patterns** instantiate or "create" objects for you.
 Examples of creational patterns are:
 
    a) **Fundamental**
 
 - [Singleton](Singleton)
 - [Builder](Builder)
 
    b) **Intermediate**
 
 - [Factory](Factory)
 - [Prototype](Prototype)
 
    c) **Advanced**
